// Fonction somme

int add (int num1 , int num2 ) {
	return num1 + num2 ;
}

// Fonction différence 

int sub (int num1 , int num2 ) {
	return num1 - num2 ;
}

// Fonction produit 

int multp (int num1 , int num2 ) {
	return num1 * num2 ;
}

// Fonction quotient 

int quot (int num1 , int num2 ) {
	return num1 / num2 ;
}

// Fonction modulo 

int modulo (int num1 , int num2 ) {
	return num1 % num2 ;
}

// Fonction et 

int et (int num1 , int num2 ) {
	return num1 & num2 ;
}

// Fonction ou 

int ou (int num1 , int num2 ) {
	return num1 | num2 ;
}

// Fonction négation 

int non (int num1 ) {
	return !num1 ;
}
